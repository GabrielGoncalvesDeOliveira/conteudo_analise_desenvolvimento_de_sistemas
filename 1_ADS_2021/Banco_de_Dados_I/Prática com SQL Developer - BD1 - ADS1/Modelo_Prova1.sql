CREATE TABLE UF
(UF CHAR(2),
DESCRICAO VARCHAR2(25) NOT NULL,
CONSTRAINT UF_PK PRIMARY KEY(UF));

CREATE TABLE CIDADE
(NUMERO NUMBER(8),
UF CHAR(2),
DESCRICAO VARCHAR2(50),
CONSTRAINT CIDADE_PK PRIMARY KEY(NUMERO),
CONSTRAINT  CIDADE_UF_FK FOREIGN KEY(UF) REFERENCES UF);

CREATE TABLE ESTADO_CIVIL
(ID_EC NUMBER(2),
DESCRICAO VARCHAR2(20),
CONSTRAINT ESTADO_CIVIL_PK PRIMARY KEY(ID_EC));

CREATE TABLE BANCO
(CODIGO NUMBER(3),
NOME VARCHAR2(50),
CONSTRAINT BANCO_PK PRIMARY KEY(CODIGO));

CREATE TABLE PESSOA
(CPF NUMBER(11), NOME VARCHAR2(70), SEXO CHAR(1),
NASCIMENTO DATE, DT_CADASTRO DATE,
EMAIL VARCHAR2(40), CELULAR VARCHAR2(15),
FIXO VARCHAR2(14), TEM_FILHOS CHAR(1),
QTD_FILHOS NUMBER(2), NUM_AGENCIA NUMBER(4),
DIG_AGENCIA CHAR(1), NUM_CONTA NUMBER(8),
DIG_CONTA CHAR(1), ID_EC NUMBER(2),
NUMCID NUMBER(8), CODBANCO NUMBER(3),
CONSTRAINT PESSOA_PK PRIMARY KEY(CPF),
CONSTRAINT ESTADO_CIVIL_FK FOREIGN KEY(ID_EC) REFERENCES ESTADO_CIVIL,
CONSTRAINT CIDADE_FK FOREIGN KEY(NUMCID) REFERENCES CIDADE,
CONSTRAINT BANCO_FK FOREIGN KEY(CODBANCO) REFERENCES BANCO,
CONSTRAINT TEM_FILHOS_CK CHECK(TEM_FILHOS='S' OR TEM_FILHOS='N'));

CREATE TABLE PAGAMENTO
(PESSOA_CPF NUMBER(11),
MES_ANO CHAR(6),
DATA_PAGTO DATE,
VALOR NUMBER(7,2),
CONSTRAINT PAGAMENTO_PK PRIMARY KEY(PESSOA_CPF, MES_ANO),
CONSTRAINT PESSOA_CPF_FK FOREIGN KEY(PESSOA_CPF) REFERENCES PESSOA); 